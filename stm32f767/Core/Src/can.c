static void canOpenInput(CAN_RxHeaderTypeDef *rx_msg,uint8_t *data)
{
	Message rxm;
	
	rxm.cob_id = rx_msg->StdId;
	rxm.len = rx_msg->DLC;
	memcpy(rxm.data, data, rx_msg->DLC);
	(rx_msg->RTR == CAN_RTR_REMOTE)? (rxm.rtr = 1) : (rxm.rtr = 0);

	osMessageQueuePut(CANRecvQueueHandle, &rxm, NULL, 0);
}


/**
  * @brief  Rx FIFO 0 message pending callback.
  * @param  hcan pointer to a CAN_HandleTypeDef structure that contains
  *         the configuration information for the specified CAN.
  * @retval None
  */
void HAL_CAN_RxFifo0MsgPendingCallback(CAN_HandleTypeDef *hcan)
{
	uint8_t data[8];
	CAN_RxHeaderTypeDef Can_RxMessage;
	
	if(hcan->Instance==CAN1)
	{
		HAL_CAN_GetRxMessage(hcan, CAN_RX_FIFO0, &Can_RxMessage, data);
		canOpenInput(&Can_RxMessage, data);
	}
}

